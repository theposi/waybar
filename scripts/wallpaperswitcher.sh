#!/bin/bas
SELECTED_THEME=$1
THEMES=$2
WAY_DIR=$3

WALLPAPERS_DIR="$WAY_DIR/wallpapers"

if [ -f "$WALLPAPERS_DIR/$SELECTED_THEME.jpg" ]; then
	if echo "$THEMES" | grep -wq "$SELECTED_THEME"; then
		swww img "$WALLPAPERS_DIR/$SELECTED_THEME.jpg" --transition-type=simple --transition-duration=0.5
	fi
fi
